private double dividir(int numero1, int numero2)
{
  return numero1/numero2;
}

private double multiplicar(int numero1, int numero2)
{
  return numero1*numero2;
}

private int suma(int numero1, int numero2)
{
	return numero1+numero2;
}

private int resta (int numero1, int numero2){
  return numero1 - numero2;
}

